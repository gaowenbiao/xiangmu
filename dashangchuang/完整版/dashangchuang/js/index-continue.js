$(window).scroll(function(){
    var oScrollTop=$(window).scrollTop();
    if(oScrollTop>$(".banner").height()){
        $(".left-nav").fadeIn(300);
    }else if(oScrollTop<$(".banner").height()){
        $(".left-nav").fadeOut(500)
    }
    var winH=$(window).height();
    if (winH + oScrollTop - $(".build1").eq(0).offset().top > winH-120 ) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(0).addClass("left-active");
    }
    if (winH + oScrollTop - $(".build1").eq(1).offset().top > winH-120) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(1).addClass("left-active");
    }
    if (winH + oScrollTop - $(".build2").eq(0).offset().top > winH-120 ) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(2).addClass("left-active");
    }
    if (winH + oScrollTop - $(".build2").eq(1).offset().top > winH-120 ) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(3).addClass("left-active");
    }
    if (winH + oScrollTop - $(".build").eq(0).offset().top > winH-120) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(4).addClass("left-active");
    }
    if (winH + oScrollTop - $(".build").eq(1).offset().top > winH-120 ) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(5).addClass("left-active");
    }
    if (winH + oScrollTop - $(".build").eq(2).offset().top > winH-120) {
        $(".left-items").removeClass("left-active");
        $(".left-items").eq(6).addClass("left-active");
    }
})
